"""End-to-end Boltz-2 AffinityModule parity on a REAL protein+ligand complex.

Pipeline (CPU is fine):
  1. Load real complex feats (outputs/real_features/affinity_complex.{pt,npz})
     generated by make_affinity_feats.py from boltz/examples/affinity.yaml
     (protein A + ligand B). affinity_token_mask has nonzero ligand entries.
  2. Build the SHARED affinity inputs ONCE on the JAX side:
       - trunk -> s_inputs, z      (boltz2_trunk_forward, conf checkpoint)
       - x_pred                     (boltz2_sample_forward; augmentation OFF,
                                     num_sampling_steps=25, fixed external noise)
     These exact numpy arrays feed BOTH frameworks, isolating affinity drift.
  3. Run JAX affinity_module_forward and the PyTorch reference AffinityModule
     (boltz2_aff.ckpt, prefix affinity_module1, config from the parity test).
  4. Compare affinity_pred_value & affinity_logits_binary: max abs diff + scalars.
  5. Write outputs/affinity_e2e.json.

Run (needs jax + torch + boltz src, i.e. the torch-bridge extra):

  JAX_PLATFORMS=cpu uv run --extra torch-bridge python scripts/benchmark_affinity_e2e.py
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

import jax
import jax.numpy as jnp
import numpy as np
import torch

from boltz_jax.bridge.affinity_mapping import map_affinity_module_state_dict
from boltz_jax.bridge.torch_checkpoint import load_checkpoint_state_dict
from boltz_jax.bridge.torch_mapping import map_boltz2_graph_state_dict
from boltz_jax.models.heads.affinity import affinity_module_forward
from boltz_jax.models.trunk_blocks.trunk import boltz2_sample_forward, boltz2_trunk_forward

ROOT = Path(__file__).resolve().parent.parent
BOLTZ_SRC = ROOT.parent / "boltz" / "src"
CACHE = ROOT.parent / "boltz" / ".cache" / "boltz"
CONF_CKPT = CACHE / "boltz2_conf.ckpt"
AFF_CKPT = CACHE / "boltz2_aff.ckpt"
FEATS_PT = ROOT / "outputs" / "real_features" / "affinity_complex.pt"
OUT_JSON = ROOT / "outputs" / "affinity_e2e.json"

# Trunk graph config (matches compare_sampling_rmsd.py / conf checkpoint).
MSA_LAYERS = 4
PAIRFORMER_LAYERS = 64
TOKEN_LAYERS = 24
RECYCLING = 3
NUM_SAMPLING_STEPS = 25
SEED = 0

# Affinity module config (from boltz2_aff.ckpt hyper_parameters; parity test).
AFF_PREFIX = "affinity_module1"
TOKEN_S = 384
TOKEN_Z = 128
PAIRFORMER_ARGS = {
    "num_blocks": 8,
    "dropout": 0.25,
    "activation_checkpointing": False,
    "use_trifast": True,
}
TRANSFORMER_ARGS = {
    "num_blocks": 12,
    "num_heads": 8,
    "token_s": TOKEN_S,
    "activation_checkpointing": False,
}
NUM_DIST_BINS = 64
MAX_DIST = 22
GROUPS = {0: 1, 1: 4, 2: 6, 3: 4, 4: 1}


def _load_feats(path: Path) -> tuple[dict[str, np.ndarray], str | None]:
    obj = torch.load(path, map_location="cpu", weights_only=False)
    record_id = obj.get("_record_id")
    feats = {
        k: v.detach().cpu().numpy()
        for k, v in obj.items()
        if not k.startswith("_") and torch.is_tensor(v)
    }
    return feats, record_id


def _build_torch_affinity(state: dict[str, torch.Tensor]) -> torch.nn.Module:
    sys.path.insert(0, str(BOLTZ_SRC))
    from boltz.model.modules.affinity import AffinityModule  # noqa: PLC0415

    module = AffinityModule(
        token_s=TOKEN_S,
        token_z=TOKEN_Z,
        pairformer_args=PAIRFORMER_ARGS,
        transformer_args=TRANSFORMER_ARGS,
        num_dist_bins=NUM_DIST_BINS,
        max_dist=MAX_DIST,
        use_cross_transformer=False,
        groups=GROUPS,
    ).eval()
    module_state = {
        key.removeprefix(f"{AFF_PREFIX}."): value
        for key, value in state.items()
        if key.startswith(f"{AFF_PREFIX}.")
    }
    module.load_state_dict(module_state)
    return module


def main() -> None:
    jax.config.update("jax_default_matmul_precision", "highest")
    jax.config.update("jax_enable_x64", False)
    torch.backends.cuda.matmul.allow_tf32 = False
    torch.backends.cudnn.allow_tf32 = False

    for p in (CONF_CKPT, AFF_CKPT, FEATS_PT, BOLTZ_SRC):
        assert p.exists(), f"missing required path: {p}"

    feats_np, record_id = _load_feats(FEATS_PT)
    aff_mask = np.asarray(feats_np["affinity_token_mask"]).astype(bool)
    n_lig = int(aff_mask.sum())
    assert n_lig > 0, "BLOCKER: affinity_token_mask all zero (no ligand tokens)"
    n_atoms = int(feats_np["atom_pad_mask"].shape[1])
    print(f"record={record_id}  n_ligand_tokens={n_lig}  n_atoms={n_atoms}")

    jax_feats = {k: jnp.asarray(v) for k, v in feats_np.items()}

    # --- Step 2: SHARED inputs from the JAX trunk + sampler (conf checkpoint).
    conf_state = load_checkpoint_state_dict(CONF_CKPT)
    trunk_params = map_boltz2_graph_state_dict(
        conf_state,
        num_msa_layers=MSA_LAYERS,
        num_pairformer_layers=PAIRFORMER_LAYERS,
        num_token_layers=TOKEN_LAYERS,
        token_transformer_heads=16,
    )

    trunk = boltz2_trunk_forward(
        trunk_params["trunk"], jax_feats, recycling_steps=RECYCLING
    )
    s_inputs_jax = trunk["s_inputs"]
    z_jax = trunk["z"]

    rng = np.random.default_rng(SEED)
    init_noise = rng.standard_normal((1, n_atoms, 3)).astype(np.float32)
    step_noises = rng.standard_normal(
        (NUM_SAMPLING_STEPS, 1, n_atoms, 3)
    ).astype(np.float32)

    sample = boltz2_sample_forward(
        trunk_params,
        jax_feats,
        jax.random.PRNGKey(SEED),
        num_sampling_steps=NUM_SAMPLING_STEPS,
        recycling_steps=RECYCLING,
        token_layers=TOKEN_LAYERS,
        augmentation=False,
        alignment_reverse_diff=True,
        init_noise=jnp.asarray(init_noise),
        step_noises=jnp.asarray(step_noises),
    )
    x_pred_jax = sample["sample_atom_coords"]

    # Freeze the shared inputs as numpy so BOTH frameworks see identical data.
    s_inputs_np = np.asarray(s_inputs_jax)
    z_np = np.asarray(z_jax)
    x_pred_np = np.asarray(x_pred_jax)
    print(
        f"shared inputs: s_inputs{s_inputs_np.shape} z{z_np.shape} "
        f"x_pred{x_pred_np.shape}"
    )

    # --- Step 3a: JAX affinity module (aff checkpoint).
    aff_state = load_checkpoint_state_dict(AFF_CKPT)
    aff_params = map_affinity_module_state_dict(aff_state, AFF_PREFIX)
    out_jax = affinity_module_forward(
        aff_params,
        jnp.asarray(s_inputs_np),
        jnp.asarray(z_np),
        jnp.asarray(x_pred_np),
        jax_feats,
        multiplicity=1,
    )

    # --- Step 3b: PyTorch reference affinity module on the SAME numpy inputs.
    torch_module = _build_torch_affinity(aff_state)
    feats_torch = {
        "token_to_rep_atom": torch.from_numpy(feats_np["token_to_rep_atom"]),
        "token_pad_mask": torch.from_numpy(feats_np["token_pad_mask"]),
        "mol_type": torch.from_numpy(feats_np["mol_type"]),
        "affinity_token_mask": torch.from_numpy(feats_np["affinity_token_mask"]),
    }
    with torch.no_grad():
        out_torch = torch_module(
            s_inputs=torch.from_numpy(s_inputs_np),
            z=torch.from_numpy(z_np),
            x_pred=torch.from_numpy(x_pred_np),
            feats=feats_torch,
            multiplicity=1,
            use_kernels=False,
        )

    # --- Step 4: compare.
    results = {}
    for key in ("affinity_pred_value", "affinity_logits_binary"):
        j = np.asarray(out_jax[key]).reshape(-1)
        t = out_torch[key].detach().cpu().numpy().reshape(-1)
        max_abs = float(np.max(np.abs(j - t)))
        results[key] = {
            "jax": [float(x) for x in j],
            "torch": [float(x) for x in t],
            "max_abs_diff": max_abs,
        }
        print(f"{key}: jax={j.tolist()} torch={t.tolist()} max|d|={max_abs:.3e}")

    payload = {
        "input_yaml": "boltz/examples/affinity.yaml (msa: empty)",
        "features_pt": str(FEATS_PT),
        "record_id": record_id,
        "n_ligand_tokens": n_lig,
        "n_atoms": n_atoms,
        "num_sampling_steps": NUM_SAMPLING_STEPS,
        "recycling_steps": RECYCLING,
        "seed": SEED,
        "augmentation": False,
        "conf_checkpoint": str(CONF_CKPT),
        "aff_checkpoint": str(AFF_CKPT),
        "results": results,
    }
    OUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    OUT_JSON.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(f"\nwrote {OUT_JSON}")


if __name__ == "__main__":
    main()
